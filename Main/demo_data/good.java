public class ThisClassIsGood {

    // Fields
    private int integer1;
    private int integer2;
    private String str1;

    /**
     * Default constructor
     */
    public ThisIsOneClass() {
        integer1 = 0;
        integer2 = 0;
        str1 = "";
    }

    /**
     * Sets all fields
     * @param newInteger1 new value for integer1
     * @param newInteger2 new value for integer2
     * @param newStr1 new value for str1
     */
    public void setAllFields(int newInteger1, int newInteger2, String newStr1) {
        integer1 = newInteger1;
        integer2 = newInteger2;
        str1 = newStr1;
    }
}